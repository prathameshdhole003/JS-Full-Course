// What is Event Object
// Event object is the parent object of event object
// Ex. MouseEvent, FocusEvent, KeyboardEvent, etc.

/* Mouse Event in JavaScript */
// Mouse Event Object -> Event that occurs when mouse interacts with HTML elements that belong to MouseEvent object

// Keyboard events in Javascript
// Events that occur when user presses a key on the keyboard belongs to the keyboard event object

// onkeydown() -> Event is triggered when the user presses a key
// onkeypress() -> Event is triggered when the user presses & releases a key
// onkeyup() -> Event is triggered when the user releases a key

// Input Events
// The onchange event occurs when value of an element is changed

// onclick() vs addEventListener()
// addEventListener() does not override any existing event handlers, whereas onclick() overrides any existing onclick() event handlers 