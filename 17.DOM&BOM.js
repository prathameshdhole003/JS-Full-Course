// The DOM is the Document Object Model which deals with the document, the HTML elements themselves
// Ex. Change the background color to red
// document.body.style.background = "red";

// The BOM is the Browser Object Model, which deals with browser components other than document, like history, location, navigator and screen 
// Functions like alert/confirm/prompt are also part of BOM. They are directly not related to document but represent pure methods of communicating with the user

// 1. document.getElementById(#id_name) -> Get the element by using id
// 2. document.getElementsByClassName(.class_name) -> Get the element by using class
// 3. document.getElementbyTagName(tag_name('p', 'h2')) -> Get element by using tag name
// 4. document.getElementsByName(name) -> Get the element by using name

// 5. document.querySelector() -> Returns the first matching element
// Can be used for both class & id

// 5. document.querySelectorAll() -> Returns all matching elements
// Can be used for both class & id
